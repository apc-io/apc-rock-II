int cmos_init_hy511(cmos_init_arg_t  *init_arg)
{
	unsigned char *array_addr,data;
	unsigned int array_size;
       printk(">> cmos_init_hy511 %d %d \n", init_arg->width, init_arg->height);

             	if (init_arg->is_full_init)
             {
                    if ((init_arg->width == 640) && ( init_arg->height == 480)){
				printk(" yacd511_640x480 init \n");
	              	array_size=sizeof(yacd511_640x480);
				array_addr=yacd511_640x480;
                    }else if((init_arg->width == 1600) && ( init_arg->height == 1200)){
				printk("init yacd511_1600x1200_key \n");
				array_size=sizeof(yacd511_1600x1200_key);
				array_addr=yacd511_1600x1200_key;
                    }else if((init_arg->width == 320) && ( init_arg->height == 240)){
				printk("init yacd511_320x240 \n");
				array_size=sizeof(yacd511_320x240);
				array_addr=yacd511_320x240;
                    }else{
                    	printk(">> [ERR] not support resolution \n");
                    	return -1;
                    }
               }else{ // key setting
               	 if ((init_arg->width == 640) && ( init_arg->height == 480)){
				printk(" yacd511_640x480_key init \n");
	              	array_size=sizeof(yacd511_640x480_key);
				array_addr=yacd511_640x480_key;
                    }else if((init_arg->width == 1600) && ( init_arg->height == 1200)){
				printk("init yacd511_1600x1200_key \n");
				array_size=sizeof(yacd511_1600x1200_key);
				array_addr=yacd511_1600x1200_key;
                    }else{
                    	printk(">[ERR] not support resolution \n");
                    	return -1;
                    }
              }
		printk("\n >>write_array_2_i2c() \n");
       	write_array_2_i2c(CMOS_HY511_I2C_ADDR, array_addr, array_size);
        
		
      wmt_vid_i2c_write( CMOS_HY511_I2C_ADDR, 0x03, 0x0);//set to page 0
	data = wmt_vid_i2c_read(CMOS_HY511_I2C_ADDR, 0x11) ;
	printk("cmos_v_flip %d cmos_h_flip %d \n", init_arg->cmos_v_flip, init_arg->cmos_h_flip);
	if (init_arg->cmos_v_flip)//y-flip
		data |= 0x2;
	
	if (init_arg->cmos_h_flip)//x-flip
		data |= 0x1;
	
      wmt_vid_i2c_write( CMOS_HY511_I2C_ADDR, 0x11, data);

      return 0;
}

